def main():
    db = SessionLocal()
    try:
        # Countries
        countries = {c.name.lower() for c in db.query(Country).all()}
        wanted = {(c or "").strip() for (c,) in db.query(Disciple.country).distinct() if c}
        wanted.add("Россия")
        added_c = 0
        for name in sorted(wanted):
            if name and name.lower() not in countries:
                db.add(Country(name=name))
                added_c += 1
        # Regions
        regions = {r.name.lower() for r in db.query(Region).all()}
        added_r = 0
        for name in RF_REGIONS:
            if name.lower() not in regions:
                db.add(Region(name=name))
                added_r += 1
        db.commit()
        print(f"[seed_geo] страны +{added_c}, регионы +{added_r}")
    finally:
        db.close()


if __name__ == "__main__":
    main()
